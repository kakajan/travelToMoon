     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $posts = Post::all();
        foreach ($posts as $post) {
            $post->likeCount = $post->likes->count();
            //return $post->likes;
            if($post->likes->where('id', $request->user()->id)->first()){
                $liked = true;
            } else {
                $liked = false;
            }
            $post->liked = $liked;
        }
        return $posts;
    }

